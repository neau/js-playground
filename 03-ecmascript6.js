// ES6 expands on exisitng primitives to fix some long time issues and add useful functionality.

String.fromCharCode(65); // A

// ES6 introduces the ability to declare binary and octal literals.
console.log(parseInt("101", 2)); // 5 in Binary
console.log(parseInt("11", 8)); //9 in Octal

// Declare a binary number using the 0b prefix
console.log(0b101); // 5

//Octal numbers can be declared using 0o
console.log(0o11); // 9
